import typing as t

from datetime import date

from pydantic import AfterValidator
from pydantic import AliasChoices
from pydantic import BaseModel
from pydantic import Field
from pydantic import field_validator
from pydantic import model_validator

from roadmap.common import ensure_date
from roadmap.data.systems import OS_LIFECYCLE_DATES


Date = t.Annotated[str | date, AfterValidator(ensure_date)]


class AppStreamPackage(BaseModel):
    name: str
    application_stream_name: str
    start_date: Date | None = None
    end_date: Date = Field(validation_alias=AliasChoices("end_date", "enddate"))
    initial_product_version: str
    os_major: int | None = Field(init=False, default=None)
    os_minor: int | None = None
    stream: str
    lifecycle: int
    rolling: bool = False

    @field_validator("initial_product_version")
    @classmethod
    def validate_version(cls, value):
        return ".".join(value.split(".")[:2])

    @model_validator(mode="after")
    def set_start_date(self):
        """If no start_date is supplied, get it from the OS lifecycle date"""
        if self.start_date is None:
            try:
                self.start_date = OS_LIFECYCLE_DATES[self.initial_product_version].start
            except KeyError:
                self.start_date = "Unknown"
                return self

        return self

    @model_validator(mode="after")
    def set_rolling(self):
        if self.lifecycle == 0 and self.end_date == date(1111, 11, 11):
            self.rolling = True

        return self

    @model_validator(mode="after")
    def set_os_version(self):
        self.os_major = int(self.initial_product_version.split(".")[0])
        try:
            self.os_minor = int(self.initial_product_version.split(".")[1])
        except IndexError:
            self.os_minor = None

        return self


